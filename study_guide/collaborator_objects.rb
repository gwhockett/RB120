Objects stored as state that work in conjunction with the object that they are stored in are
called Collaborator Objects. Collaborator objects are usually custom objects defined by the
programmer and not inherited from the Ruby core library. Collaboration between objects gives
programmers a way to make useful connections between various actors of a program. Collaborator
objects is at the core of OOP by enabling a program to be chop up and modularize the task
of a program into cohesive pieces.


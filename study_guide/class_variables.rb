=begin
Class variables capture information about an entire class and are shared with
all instances of the respective class. They are created using two `@` symbols
in front of a snake cased name, like this: `@@snake_case` Class variables can
be directly initialized at the class level. Class variables can be accessed
using instance and class methods. All objects of a class share one copy of a
class variable. Instances of a class can use instance methods to access a
class variable outside of a class.
=end